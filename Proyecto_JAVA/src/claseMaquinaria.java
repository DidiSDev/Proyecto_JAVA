
public class claseMaquinaria {
	private int idMaquinaria, idProveedor;
	private String nombre, fechaAdquisicion, eatado;
	
	public int getIdMaquinaria() {
		return idMaquinaria;
	}
	public void setIdMaquinaria(int idMaquinaria) {
		this.idMaquinaria = idMaquinaria;
	}
	public int getIdProveedor() {
		return idProveedor;
	}
	public void setIdProveedor(int idProveedor) {
		this.idProveedor = idProveedor;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getFechaAdquisicion() {
		return fechaAdquisicion;
	}
	public void setFechaAdquisicion(String fechaAdquisicion) {
		this.fechaAdquisicion = fechaAdquisicion;
	}
	public String getEatado() {
		return eatado;
	}
	public void setEatado(String eatado) {
		this.eatado = eatado;
	}
}
